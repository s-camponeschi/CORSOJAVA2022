package srl.neotech.corsojava.scacchi;

public class Giocatore {
	
	private String nome;
	private Integer anni;
	private Integer livelloAbilita;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Integer getAnni() {
		return anni;
	}
	public void setAnni(Integer anni) {
		this.anni = anni;
	}
	public Integer getLivelloAbilita() {
		return livelloAbilita;
	}
	public void setLivelloAbilita(Integer livelloAbilita) {
		this.livelloAbilita = livelloAbilita;
	}
	

	

}
